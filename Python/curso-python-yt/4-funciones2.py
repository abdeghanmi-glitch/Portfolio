def suma(num1, num2): #definimos la funcion con dos parametros
    resultado=num1+num2 #calculamos la suma de los dos numeros
    return resultado #devolvemos el resultado de la suma

almacena_resultado=suma(5, 7) #almacenamos el resultado de la funcion en una variable
print(almacena_resultado) #imprimimos el resultado almacenado

suma(5, 7) #llamamos a la funcion para que se ejecute

suma(10, 20) #podemos llamar a la funcion con diferentes argumentos

suma(3.14, 2.71) #podemos llamar a la funcion con diferentes tipos de datos

